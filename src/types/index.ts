// --- Party ---
export type PartyId = 'S' | 'M' | 'SD' | 'C' | 'V' | 'KD' | 'L' | 'MP'

export interface PartyInfo {
  id: PartyId
  color: string
  textColor?: string
  bloc: 'left' | 'right' | 'other'
}

export const PARTIES: PartyInfo[] = [
  { id: 'S',  color: '#E8112d', bloc: 'left' },
  { id: 'V',  color: '#DA291C', bloc: 'left' },
  { id: 'MP', color: '#83CF39', bloc: 'left' },
  { id: 'M',  color: '#1B2D6B', bloc: 'right' },
  { id: 'SD', color: '#dddd00', textColor: '#111', bloc: 'right' },
  { id: 'KD', color: '#231977', bloc: 'right' },
  { id: 'C',  color: '#009933', bloc: 'right' },
  { id: 'L',  color: '#006AB3', bloc: 'right' },
]

export function getParty(id: string): PartyInfo | undefined {
  return PARTIES.find(p => p.id === id.toUpperCase())
}

